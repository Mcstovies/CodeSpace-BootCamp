//Defining the number values
let x = 2;
let y = 3;
let z = 4;

// Checking if all numbers are equal
if (x === y && y === z) {
    console.log("All numbers are equal.");
} 
// Checking if all numbers are different
else if (x !== y && x !== z && y !== z) {
    console.log("All numbers are different.");
} 
// If neither all are equal nor different
else {
    console.log("Neither all are equal nor different.");
}
